"use client";

export const dynamic = "force-dynamic";

import { useEffect, useState } from "react";
import { useRouter }           from "next/navigation";
import { signInWithPopup }              from "firebase/auth";
import { getAuthInstance, googleProvider } from "@/lib/firebase";
import { useAuth }              from "@/hooks/useAuth";
import { saveUserProfile }      from "@/lib/firestore";

export default function LoginPage() {
  const { user, loading } = useAuth();
  const router = useRouter();
  const [signingIn, setSigningIn] = useState(false);
  const [error,     setError]     = useState<string | null>(null);

  useEffect(() => {
    if (!loading && user) {
      router.replace("/");
    }
  }, [user, loading, router]);

  async function handleGoogleSignIn() {
    setSigningIn(true);
    setError(null);
    try {
      const result = await signInWithPopup(getAuthInstance(), googleProvider);
      const u = result.user;

      // Create/update user profile on first sign-in
      await saveUserProfile(u.uid, {
        name:                 u.displayName || "",
        email:                u.email       || "",
        notificationsEnabled: false,
        notificationTime:     "20:00",
      });

      router.replace("/");
    } catch (e: unknown) {
      const msg = e instanceof Error ? e.message : "Sign-in failed.";
      setError(msg.includes("popup-closed") ? null : "Sign-in failed. Try again.");
    } finally {
      setSigningIn(false);
    }
  }

  if (loading) {
    return (
      <div className="min-h-screen bg-studio flex items-center justify-center">
        <span className="georgia-text text-cream/30 text-lg animate-pulse">
          For the Record
        </span>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-studio flex flex-col items-center justify-center px-4">
      <div className="w-full max-w-sm text-center animate-fade-up">

        {/* Brand mark */}
        <div className="mb-10">
          <div className="inline-flex items-center justify-center w-16 h-16 rounded-xl bg-red mb-6">
            <span className="font-georgia font-bold text-white text-xs tracking-widest">
              ON<br />AIR
            </span>
          </div>
          <h1 className="font-georgia text-3xl font-bold text-cream mb-2">
            For the Record
          </h1>
          <p className="text-cream/40 text-sm font-sans leading-relaxed">
            Your life is worth recording. Every day.
          </p>
        </div>

        {/* Sign in */}
        <button
          onClick={handleGoogleSignIn}
          disabled={signingIn}
          className="w-full flex items-center justify-center gap-3 py-3 px-6 rounded bg-cream text-dark-brown font-sans font-semibold text-sm transition-warm hover:bg-cream-dark active:scale-[0.98] disabled:opacity-60"
        >
          {/* Google icon */}
          <svg width="18" height="18" viewBox="0 0 18 18" aria-hidden="true">
            <path fill="#4285F4" d="M17.64 9.2c0-.637-.057-1.251-.164-1.84H9v3.481h4.844c-.209 1.125-.843 2.078-1.796 2.717v2.258h2.908c1.702-1.567 2.684-3.875 2.684-6.615z"/>
            <path fill="#34A853" d="M9 18c2.43 0 4.467-.806 5.956-2.18l-2.908-2.259c-.806.54-1.837.86-3.048.86-2.344 0-4.328-1.584-5.036-3.711H.957v2.332A8.997 8.997 0 0 0 9 18z"/>
            <path fill="#FBBC05" d="M3.964 10.71A5.41 5.41 0 0 1 3.682 9c0-.593.102-1.17.282-1.71V4.958H.957A8.996 8.996 0 0 0 0 9c0 1.452.348 2.827.957 4.042l3.007-2.332z"/>
            <path fill="#EA4335" d="M9 3.58c1.321 0 2.508.454 3.44 1.345l2.582-2.58C13.463.891 11.426 0 9 0A8.997 8.997 0 0 0 .957 4.958L3.964 7.29C4.672 5.163 6.656 3.58 9 3.58z"/>
          </svg>
          {signingIn ? "Signing inâ€¦" : "Continue with Google"}
        </button>

        {error && (
          <p className="mt-4 text-sm text-red/70 animate-fade-in">{error}</p>
        )}

        <p className="mt-8 text-xs text-cream/20 font-sans leading-relaxed">
          Your stories are private and belong to you.
        </p>
      </div>
    </div>
  );
}
